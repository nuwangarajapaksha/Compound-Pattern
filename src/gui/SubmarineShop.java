    private void undoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_undoButtonActionPerformed
        // TODO add your handling code here:cartHistory.undo(cart);
        cartHistory.undo(cart);
        Vector model = cart.getModel();
        DefaultTableModel dtm2 = (DefaultTableModel) cartTable.getModel();
        dtm2.setRowCount(0);

//      Iterator Pattern
        Iterator iterator = model.iterator();
        while (iterator.hasNext()) {
            Vector next = (Vector) iterator.next();
            dtm2.addRow(next);
        }

        calculateTotal();
    }//GEN-LAST:event_undoButtonActionPerformed

    private void redoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_redoButtonActionPerformed
        // TODO add your handling code here:
        cartHistory.redo(cart);
        Vector model = cart.getModel();
        DefaultTableModel dtm2 = (DefaultTableModel) cartTable.getModel();
        dtm2.setRowCount(0);

        Iterator iterator = model.iterator();
        while (iterator.hasNext()) {
            Vector next = (Vector) iterator.next();
            dtm2.addRow(next);
        }

        calculateTotal();
    }//GEN-LAST:event_redoButtonActionPerformed

