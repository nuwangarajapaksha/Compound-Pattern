    private void paidTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_paidTextFieldKeyReleased
        // TODO add your handling code here:
        double total = Double.parseDouble(totalTextField.getText());
        double paid = Double.parseDouble(paidTextField.getText());

        balanceTextField.setText(String.valueOf(paid - total));

        if (paid >= total) {
            checkoutButton.setEnabled(true);
        }


    }//GEN-LAST:event_paidTextFieldKeyReleased

    private void paidTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_paidTextFieldKeyTyped
        // TODO add your handling code here:
        if (((Character.isDigit(evt.getKeyChar())) | (String.valueOf(evt.getKeyChar()).equals(".")))) {
            if (paidTextField.getText().contains(".")) {
                if (!Character.isDigit(evt.getKeyChar())) {
                    evt.consume();
                }
            }
        } else {
            evt.consume();
        }
    }//GEN-LAST:event_paidTextFieldKeyTyped

