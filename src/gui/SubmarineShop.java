    private void calculateTotal() {
        double total = 0.0;
        for (int i = 0; i < cartTable.getRowCount(); i++) {
            total = total + Double.parseDouble(cartTable.getValueAt(i, 1).toString());
        }
        totalTextField.setText(String.valueOf(total));
    }

