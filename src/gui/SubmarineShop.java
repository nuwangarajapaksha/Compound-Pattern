    private void addToCartButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToCartButtonActionPerformed
        // TODO add your handling code here:

        int selectedRow = bunTypesTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Select a Bun Type", "Information", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        String keyWord = bunTypesTable.getValueAt(selectedRow, 0).toString();

        switch (keyWord) {
            case "Crust Bun":
                this.currentSubmarine = new CrustBun("-"+keyWord, bunTypes.get(keyWord));
                break;
            case "Soft Bun":
                this.currentSubmarine = new SoftBun("-"+keyWord, bunTypes.get(keyWord));
                break;
            case "Thick Bun":
                this.currentSubmarine = new ThickBun("-"+keyWord, bunTypes.get(keyWord));
                break;
        }

        if (mexicanSubmarineRadioButton.isSelected()) {
            MexicanSubmarineMaker mexicanSubmarineMaker = new MexicanSubmarineMaker(currentSubmarine);
            currentSubmarine = mexicanSubmarineMaker.prepare();
        } else if (italianSubmarineRadioButton.isSelected()) {
            ItalianSubmarineMaker italianSubmarineMaker = new ItalianSubmarineMaker(currentSubmarine);
            currentSubmarine = italianSubmarineMaker.prepare();
        } else if (germanSubmarineRadioButton.isSelected()) {
            GermanSubmarineMaker germanSubmarineMaker = new GermanSubmarineMaker(currentSubmarine);
            currentSubmarine = germanSubmarineMaker.prepare();
        }

        if (cartTable.getRowCount() == 0) {
            DefaultTableModel dtm = (DefaultTableModel) cartTable.getModel();
            dtm.addRow(new Vector());
            addNewButton.setEnabled(true);
            undoButton.setEnabled(true);
            redoButton.setEnabled(true);
        }
        int rowCount = cartTable.getRowCount();
        cartTable.setValueAt(currentSubmarine.getDescription(), rowCount - 1, 0);
        cartTable.setValueAt(currentSubmarine.getCost(), rowCount - 1, 1);
        calculateTotal();

        Vector<Vector> dataVector = ((DefaultTableModel) cartTable.getModel()).getDataVector();
        Vector<Vector> cartVector = new Vector<>();
        Iterator<Vector> iterator = dataVector.iterator();
        while (iterator.hasNext()) {
            cartVector.add(iterator.next());
        }
        cart.setCart(cartVector);
        cartHistory.saveHistory(cart);
    }//GEN-LAST:event_addToCartButtonActionPerformed

    private void addToBunButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToBunButtonActionPerformed
        // TODO add your handling code here:

        int selectedRow = toppingsTable.getSelectedRow();

        if (currentSubmarine == null) {
            JOptionPane.showMessageDialog(this, "Add to cart a Submarine First", "Information", JOptionPane.INFORMATION_MESSAGE);
            return;
        } else if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Select a Topping", "Information", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        String keyWord = toppingsTable.getValueAt(selectedRow, 0).toString();
        switch (keyWord) {
            case "Bacon":
                this.currentSubmarine = new Bacon(currentSubmarine, toppingTypes.get(keyWord));
                break;
            case "Cheese":
                this.currentSubmarine = new Cheese(currentSubmarine, toppingTypes.get(keyWord));
                break;
            case "Chicken":
                this.currentSubmarine = new Chicken(currentSubmarine, toppingTypes.get(keyWord));
                break;
            case "Ham":
                this.currentSubmarine = new Ham(currentSubmarine, toppingTypes.get(keyWord));
                break;
            case "Ketchup":
                this.currentSubmarine = new Ketchup(currentSubmarine, toppingTypes.get(keyWord));
                break;
            case "Levites":
                this.currentSubmarine = new Levites(currentSubmarine, toppingTypes.get(keyWord));
                break;
            case "Onion":
                this.currentSubmarine = new Onion(currentSubmarine, toppingTypes.get(keyWord));
                break;
            case "Tomato":
                this.currentSubmarine = new Tomato(currentSubmarine, toppingTypes.get(keyWord));
                break;

        }

        int rowCount = cartTable.getRowCount();
        cartTable.setValueAt(currentSubmarine.getDescription(), rowCount - 1, 0);
        cartTable.setValueAt(currentSubmarine.getCost(), rowCount - 1, 1);
        calculateTotal();

        Vector<Vector> dataVector = ((DefaultTableModel) cartTable.getModel()).getDataVector();
        Vector<Vector> cartVector = new Vector<>();
        Iterator<Vector> iterator = dataVector.iterator();
        while (iterator.hasNext()) {
            cartVector.add(iterator.next());
        }
        cart.setCart(cartVector);
        cartHistory.saveHistory(cart);
    }//GEN-LAST:event_addToBunButtonActionPerformed

